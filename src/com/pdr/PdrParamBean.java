package com.pdr;

import java.util.Date;

public class PdrParamBean {
	private String currentDir; /* Directory from which recusrsing will start */
	private Date fromDate; /* Date from which report is required */
	public PdrParamBean() {
		
	}
	public PdrParamBean(String currentDir, Date fromDate) {
		super();
		this.currentDir = currentDir;
		this.fromDate = fromDate;
	}
	public String getCurrentDir() {
		return currentDir;
	}
	public void setCurrentDir(String currentDir) {
		this.currentDir = currentDir;
	}
	public Date getFromDate() {
		return fromDate;
	}
	public void setFromDate(Date fromDate) {
		this.fromDate = fromDate;
	}
}
